def quadratic(a,b,c):
    d = b**2 - 4*a*c
    if d <= 0:
        return "no real roots"
    
    x1 = (-b + d**0.5) / (2*a)
    x2 = (-b - d**0.5) / (2*a)
    return x1, x2

print(quadratic(1, -5, 6))





















































































































































    